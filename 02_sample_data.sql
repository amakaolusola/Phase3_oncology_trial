-- ============================================================
-- DOSING (~500 rows)
-- 3 cycles x doses per patient
-- Some patients have held doses, deviations flagged
-- Protocol dose based on BSA x 75 mg/m² (investigational)
-- or BSA x 175 mg/m² for paclitaxel (control)
-- ============================================================

INSERT INTO dosing (patient_id, visit_id, cycle_number, day_in_cycle, protocol_dose_mg, administered_dose_mg, dose_held, deviation_flag, deviation_reason)
SELECT
    p.patient_id,
    v.visit_id,
    v.visit_number - 1 AS cycle_number,
    1 AS day_in_cycle,
    CASE
        WHEN p.treatment_arm = 'Investigational' THEN ROUND((p.bsa * 75)::numeric, 1)
        ELSE ROUND((p.bsa * 175)::numeric, 1)
    END AS protocol_dose_mg,
    CASE
        WHEN v.visit_status = 'Missed' OR v.visit_status = 'Withdrawn' THEN NULL
        WHEN p.patient_id IN (7, 10, 27, 50, 65, 85, 100) AND v.visit_number >= 3 THEN NULL
        WHEN p.patient_id IN (3, 17, 23, 25, 37, 45, 56, 72, 92, 96, 106, 112) AND v.visit_number = 4 THEN
            ROUND((CASE WHEN p.treatment_arm = 'Investigational' THEN p.bsa * 75 * 0.75 ELSE p.bsa * 175 * 0.75 END)::numeric, 1)
        ELSE
            ROUND((CASE WHEN p.treatment_arm = 'Investigational' THEN p.bsa * 75 ELSE p.bsa * 175 END)::numeric, 1)
    END AS administered_dose_mg,
    CASE
        WHEN v.visit_status IN ('Missed', 'Withdrawn') THEN FALSE
        WHEN p.patient_id IN (7, 10, 27, 50, 65, 85, 100) AND v.visit_number >= 3 THEN TRUE
        ELSE FALSE
    END AS dose_held,
    CASE
        WHEN p.patient_id IN (3, 17, 23, 25, 37, 45, 56, 72, 92, 96, 106, 112) AND v.visit_number = 4 THEN TRUE
        ELSE FALSE
    END AS deviation_flag,
    CASE
        WHEN p.patient_id IN (3, 17, 23, 25, 37, 45, 56, 72, 92, 96, 106, 112) AND v.visit_number = 4 THEN '25% dose reduction due to Grade 3 adverse event'
        ELSE NULL
    END AS deviation_reason
FROM patients p
JOIN visits v ON p.patient_id = v.patient_id
WHERE v.visit_number > 1;  -- Exclude baseline — no dosing at baseline


